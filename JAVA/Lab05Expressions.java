public class Lab05Expressions
{
        public static void main(String[] args)
        {
                int a = 3, b = 10, c = 7;
                double w = 12.9, y = 3.2;
                System.out.println("Expressions: a + b * c = " + (a + b * c));
                System.out.println("Expressions: a - b - c = " + (a - b - c));
                System.out.println("Expressions: a / b = " + (a / b));
                System.out.println("Expressions: b / a = " + (b / a));
                System.out.println("Expressions: a - b / c = " + (a - b / c));
                System.out.println("Expressions: w / y = " + (w / y));
                System.out.println("Expressions: y / w = " + (y / w));
                System.out.println("Expressions: a + w / b = " + (a + w / b));
                System.out.println("Expressions: a % b / y = " + (a % b / y));
                System.out.println("Expressions: b % a = " + (b % a));
                System.out.println("Expressions: w % y = " + (w % y));
        
        }     
}